// Addition function
function add(a, b) {
  return a + b;
}

// Subtract function
function subtract(a, b) {
  return a - b;
}

// Multiply function
function multiply(a, b) {
  return a * b;
}
// Divide function
function divide(a, b) {
  return a / b;
}
// increment by one
function increment(a) {
  return a + 1;
}

// decrement by one
function decrement(a) {
  return a - 1;
}

// parses as integer
function makeInt(a) {
  return parseInt(a, 10);
}

// parses as a floating point number
function preserveDecimal(n) {
  return parseFloat(n);
}
